export let currentLang = 'fr';

export function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('mxLang', lang);
  document.querySelectorAll('.lang-btn').forEach(btn => btn.classList.remove('active'));
  const activeBtn = document.getElementById('lang-' + lang);
  if (activeBtn) activeBtn.classList.add('active');
  const cityInput = document.getElementById('cityInput');
  const currentCity = cityInput ? cityInput.value.trim() : '';
  const hasWeather = window.HAS_WEATHER || !!document.querySelector('.current-weather');
  if (currentCity && hasWeather) {
    const unit = document.getElementById('unitInput') ? document.getElementById('unitInput').value : 'C';
    window.location.href = `/?city=${encodeURIComponent(currentCity)}&unit=${unit}&lang=${lang}`;
  } else {
    document.body.classList.add('switching');
    setTimeout(() => {
      applyLanguage(lang);
      document.body.classList.remove('switching');
    }, 180);
  }
  const langInput = document.getElementById('langInput');
  if (langInput) langInput.value = lang;
}
window.setLanguage = setLanguage;

export function applyLanguage(lang) {
  const t = TRANSLATIONS[lang] || TRANSLATIONS.fr;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] === undefined) return;
    // Capture a leading icon (Tabler <i>, Meteocons <img>, flag <span>) once, so
    // re-translating text never wipes the icon that sits next to the label.
    if (el._mxIconPrefix === undefined) {
      const html = el.innerHTML;
      const m = html.match(/^\s*(?:<!--[^>]*-->)?\s*(<i class="ti[^"]*"[^>]*><\/i>|<img class="weather-icon-meteocon"[^>]*>|<span class="fi[^"]*"[^>]*><\/span>)/);
      el._mxIconPrefix = m ? m[1] : '';
    }
    if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
      el.placeholder = t[key];
    } else {
      el.innerHTML = (el._mxIconPrefix ? el._mxIconPrefix + ' ' : '') + t[key];
    }
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (t[key]) el.placeholder = t[key];
  });
  const searchInput = document.getElementById('cityInput');
  if (searchInput && t.search_placeholder) searchInput.placeholder = t.search_placeholder;
  if (typeof window.updateFavButton === 'function') window.updateFavButton();
}

export function initLanguage() {
  const saved = localStorage.getItem('mxLang');
  const urlLang = new URLSearchParams(window.location.search).get('lang');
  const initialLang = urlLang || saved || 'fr';
  currentLang = initialLang;
  document.querySelectorAll('.lang-btn').forEach(btn => btn.classList.remove('active'));
  const btn = document.getElementById('lang-' + initialLang);
  if (btn) btn.classList.add('active');
  applyLanguage(initialLang);
}

