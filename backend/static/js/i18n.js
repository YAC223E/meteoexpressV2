export function showExplain(key) {
  const modal = document.getElementById('explainModal');
  const titleEl = document.getElementById('explainTitle');
  const bodyEl = document.getElementById('explainBody');
  const explains = currentLang === 'en' ? EXPLAINS_EN : EXPLAINS_FR;
  const data = explains[key];
  if (!modal || !data) return;
  titleEl.textContent = data.title;
  bodyEl.innerHTML = data.body;
  modal.style.display = 'flex';
}
window.showExplain = showExplain;

export function hideExplain() {
  const modal = document.getElementById('explainModal');
  if (modal) modal.style.display = 'none';
}
window.hideExplain = hideExplain;

document.addEventListener('keydown', e => {
  if (e.key === 'Escape') hideExplain();
});
